#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	a;

	a = 0;
	while(s1[a] == s2[a] && s1[a] && s2[a] && a < n)
	{
		a++;
	}
	// if (a == n)
	// {
	// 	return (0);
	// }
	return (s1[a] - s2[a]);
}
