#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*ptr;
	int	a;

	a = 0;
	ptr = (unsigned char *)s;
	while(n--)
	{
		*ptr = 0;
		ptr++;
		a++;
	}
	ptr[a] = '\0';
}
